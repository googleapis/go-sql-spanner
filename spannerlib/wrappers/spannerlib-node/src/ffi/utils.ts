import bindings from 'bindings';
const addon = bindings('spanner_napi');

/**
 * Payload encoding strategy identifier for JSON serialization.
 */
export const ENCODING_JSON = 0;

/**
 * Payload encoding strategy identifier for Protobuf serialization (Default).
 */
export const ENCODING_PROTOBUF = 1;

/**
 * Represents the structured, user-friendly outcome of a successful native Go SDK invocation.
 * The raw, cryptic `r0`–`r4` CGO sequential struct is parsed and mapped into this interface.
 *
 * @property {number} objectId - The active numeric resource ID returned by Go for the newly created or manipulated entity (e.g., the Pool ID, Connection ID, or Rows iterator ID).
 * @property {number} pinnerId - The internal memory lock pointer ID. Used by the Go SDK to keep the returned buffer pinned in memory until it's safely copied.
 * @property {Buffer | null} protobufBytes - The raw response payload bytes (e.g., query result sets or serialized protobuf structures).
 */
export interface HandledResult {
  objectId: number;
  pinnerId: number;
  protobufBytes: Buffer | null;
}

/**
 * Represents the low-level CGO return struct generated by the underlying Go shared library.
 * Because Go natively supports multiple return values, the CGO compiler exports them
 * into a sequential C-struct. This interface maps directly to those output parameters:
 *
 * @property {number} r0 - Pinner ID. An internal pointer reference to a pinned Go memory block (locked for garbage collection safety). If > 0, the native bridge must manually release it after copying raw data.
 * @property {number} r1 - Native Error Code. The numeric status code of the native operation (0 signifies success, non-zero signifies a native Go Spanner error).
 * @property {number} r2 - Object Handle ID. The generated resource ID representing the Go database object (Pool ID, Connection ID, or Rows ID).
 * @property {number} r3 - Buffer Size / Length. The byte length of the returned data payload or native error string in `r4`.
 * @property {Buffer | null} r4 - Payload Pointer. The raw memory buffer containing the serialized protobuf data, JSON response, or native error payload.
 */
interface AddonResult {
  r0: number;
  r1: number;
  r2: number;
  r3: number;
  r4: Buffer | null;
}

/**
 * Asynchronously dispatches a function call to the native C++ N-API addon layer, bridging
 * execution to the Go Spanner shared library in a background OS worker thread.
 *
 * @param {string} funcName - The name of the native function exposed by the C++ addon to invoke (e.g., 'CreatePool', 'Execute').
 * @param {...unknown[]} args - The sequential arguments to pass to the native C++ handler.
 * @returns {Promise<HandledResult>} A Promise that resolves to the structured `HandledResult`, or rejects with a `SpannerLibError` if the native operation fails.
 */
function invokeAsync(
  funcName: string,
  ...args: unknown[]
): Promise<HandledResult> {
  return new Promise((resolve, reject) => {
    const callback = (err: unknown, result: AddonResult) => {
      if (err) {
        return reject(err);
      }
      if (result.r1 !== 0) {
        if (result.r4 && result.r3 > 0) {
          const errorJson = result.r4.toString('utf8');
          try {
            const parsed = JSON.parse(errorJson);
            return reject(new SpannerLibError(parsed.message || errorJson));
          } catch {
            return reject(new SpannerLibError(errorJson));
          }
        }
        return reject(
          new SpannerLibError(`Native Spanner Error Code: ${result.r1}`)
        );
      }

      resolve({
        objectId: result.r2,
        pinnerId: result.r0,
        protobufBytes: result.r4,
      });
    };

    addon[funcName](...args, callback);
  });
}

/**
 * The Foreign Function Interface (FFI) dispatcher instance.
 * Houses the async dispatcher and synchronous native release hooks for the underlying C++ N-API bridge.
 */
export const ffi = {
  /**
   * Dispatches an asynchronous native invocation to the Go/C++ layer on a background thread.
   */
  invokeAsync,
  /**
   * Synchronously invokes a native memory unpin operation for a locked Go pointer handle.
   */
  Release: addon.Release,
};

/**
 * Custom error class representing an exception thrown by the native Go Spanner library wrapper.
 */
export class SpannerLibError extends Error {
  constructor(message: string) {
    super(message);
    this.name = 'SpannerLibError';
  }
}
